namespace Rocketeer\Services\Events\Listeners;

trait TaggableListenerTrait
{
    /**
     * @var string
     */
    protected $tag;

    /**
     * @param string $tag
     */
    public function setTag($tag)
    {
        $this->tag = $tag;
    }

    /**
     * @return string
     */
    public function getTag()
    {
        return $this->tag;
    }
}
